package net.java.amateras.db.ui.bean;

import java.io.Serializable;

public class SaveConfig implements Serializable {

	private String key;
	private String packageIn;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public void setPackage(String packageIn) {
		this.packageIn = packageIn;

	}

	public String getPackAge() {
		return packageIn;
	}

}
